// Utilidades
export function getAdoptionDogsByRegion(region: string): AdoptionDog[] {
    return adoptionDogs.filter(dog => dog.shelterRegion === region);
}

export function getUrgentAdoptionDogs(): AdoptionDog[] {
    return adoptionDogs.filter(dog => dog.isUrgent);
}

export function getFeaturedAdoptionDogs(): AdoptionDog[] {
    return adoptionDogs.filter(dog => dog.isFeatured);
}

