export type KennelServiceType =
    | "hotel"
    | "guarderia"
    | "residencia"
    | "adiestramiento"
    | "peluqueria"
    | "veterinario"
    | "recogida_domicilio"
    | "piscina"
    | "parques"
    | "webcam"
    | "alimentacion_premium";

export type KennelType = "residencia" | "hotel" | "guarderia" | "cuidador_particular";

export interface Kennel {
    id: string;
    name: string;
    type: KennelType;
    region: string;
    country: "ES" | "FR";
    city?: string;
    address?: string;
    description: string;
    services: KennelServiceType[];
    website?: string;
    email?: string;
    phone?: string;
    priceRange?: string; // e.g., "15-25€/día"
    capacity?: number; // Número de plazas
    areaSize?: string; // e.g., "10.000m²"
    features?: string[]; // Características especiales
    // Location - GeoJSON format [longitude, latitude]
    coordinates?: [number, number];
    isVerified: boolean;
    rating?: number; // 1-5
    hasHeating?: boolean;
    acceptsCats?: boolean;
    acceptsSmallDogsOnly?: boolean;
    is24h?: boolean;
}

